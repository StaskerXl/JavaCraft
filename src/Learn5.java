public class Learn5 {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        System.out.println(x);
        System.out.println(y);

        //Оператор И
        System.out.println(x & y);
        //Оператор Или
        System.out.println(x | y);
        //Оператор Исключения
        System.out.println(x ^ y);
        //Оператор короткой цепочки И
        System.out.println(x && y);
        System.out.println((x == y) && (y != x));
        //Оператор короткой цепочки Или
        System.out.println(x || y);
        //Оператор НЕ
        System.out.println(!x);
        System.out.println(!y);


    }
}
